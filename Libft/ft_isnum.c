#include "libft.h"

int	ft_isnum(char *str)
{
	if (str == NULL)
		return (0);
	while (*str != '\0' && *str >= '0' && *str <= '9')
		str++;
	if (*str == '\0')
		return (1);
	else
		return (0);
}
